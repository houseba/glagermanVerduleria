    private void cargarHistorial() {
        DefaultTableModel model = (DefaultTableModel) tblHistorial.getModel();
        model.setRowCount(0);  // limpiar tabla

        String sql =
            "SELECT p.nombre_producto AS producto, " +
            "a.cantidad_ajustada, " +
            "a.fecha_ajuste, " +
            "a.motivo_ajuste " +
            "FROM ajuste_inventario a " +
            "JOIN Producto p ON a.cod_producto = p.cod_producto " +
            "ORDER BY a.fecha_ajuste DESC";

        try (Connection con = ConexionDB.getConexion();
             PreparedStatement ps = con.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            if (!rs.next()) {
                JOptionPane.showMessageDialog(this,
                    "No hay ajustes de stock registrados a√∫n.");
                return;
            }
            do {
                String nombreProducto = rs.getString("producto");
                double cantidad = rs.getDouble("cantidad_ajustada");
                String fecha = rs.getString("fecha_ajuste");
                String motivo = rs.getString("motivo_ajuste");

                model.addRow(new Object[]{nombreProducto, cantidad, fecha, motivo});
            } while (rs.next());

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar historial de ajustes: " + e.getMessage());
            e.printStackTrace();
        }
    }


